package Bean;

public class SubAttendance {
	String sId;
	String sName;
	int sum;
	String currentAttendance;
	public String getsId() {
		return sId;
	}
	public void setsId(String sId) {
		this.sId = sId;
	}
	public String getsName() {
		return sName;
	}
	public void setsName(String sName) {
		this.sName = sName;
	}
	public int getSum() {
		return sum;
	}
	public void setSum(int sum) {
		this.sum = sum;
	}
	public String getCurrentAttendance() {
		return currentAttendance;
	}
	public void setCurrentAttendance(String currentAttendance) {
		this.currentAttendance = currentAttendance;
	}
}
